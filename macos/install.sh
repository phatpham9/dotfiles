###############################################################################
# Preferences -> Sharing
###############################################################################

# Set computer name
sudo scutil --set ComputerName "mac"
sudo scutil --set HostName "mac"
sudo scutil --set LocalHostName "mac"
sudo defaults write /Library/Preferences/SystemConfiguration/com.apple.smb.server NetBIOSName -string "mac"
sudo defaults write /Library/Preferences/SystemConfiguration/com.apple.smb.server ServerDescription -string "mac"

###############################################################################
# Preferences -> General
###############################################################################

# Appearance
defaults write NSGlobalDomain AppleInterfaceStyle -string "Dark"

# Accent color
defaults write NSGlobalDomain AppleAccentColor -int -1

# Highlight color
defaults write NSGlobalDomain AppleHighlightColor -string "0.847059 0.847059 0.862745 Graphite"

# Sidebar icon size
defaults write NSGlobalDomain NSTableViewDefaultSizeMode -int 1

